// Изчислява суров скор за даден списък от въпроси
function computePartialScores(answers, questionIds) {
  var scores = { extra: 0, emotion: 0, move: 0, cerebro: 0 };
  (questionIds || []).forEach(function(qid) {
    var q = null;
    for (var i = 0; i < QUESTIONS.length; i++) {
      if (QUESTIONS[i].id === qid) { q = QUESTIONS[i]; break; }
    }
    if (!q || q.type !== 'radio') return;
    var idx = answers[qid];
    if (idx === undefined || idx === null) return;
    var ans = q.answers[idx];
    if (!ans || !ans.scores) return;
    Object.keys(ans.scores).forEach(function(p) {
      scores[p] += ans.scores[p];
    });
  });
  return scores;
}

// Изчислява максималния теоретичен скор за всеки продукт от дадени въпроси
function getMaxScores(questionIds) {
  var maxes = { extra: 0, emotion: 0, move: 0, cerebro: 0 };
  (questionIds || []).forEach(function(qid) {
    var q = null;
    for (var i = 0; i < QUESTIONS.length; i++) {
      if (QUESTIONS[i].id === qid) { q = QUESTIONS[i]; break; }
    }
    if (!q || q.type !== 'radio') return;
    var qMaxes = { extra: 0, emotion: 0, move: 0, cerebro: 0 };
    q.answers.forEach(function(ans) {
      Object.keys(ans.scores || {}).forEach(function(p) {
        if (ans.scores[p] > qMaxes[p]) qMaxes[p] = ans.scores[p];
      });
    });
    Object.keys(qMaxes).forEach(function(p) { maxes[p] += qMaxes[p]; });
  });
  return maxes;
}

// Определя водещия продукт и версията (low/medium/high) за TR екран
// cumulativeIds: всички въпроси ОТ НАЧАЛОТО до текущия TR
function getTRInfo(answers, cumulativeIds) {
  var scores = computePartialScores(answers, cumulativeIds);
  var maxes  = getMaxScores(cumulativeIds);
  var keys   = ['extra', 'emotion', 'move', 'cerebro'];

  var leader = keys.reduce(function(a, b) {
    return scores[b] > scores[a] ? b : a;
  });

  var leaderScore = scores[leader];
  var leaderMax   = maxes[leader];
  var version;

  if (leaderMax === 0 || leaderScore === 0) {
    version = 'low';
  } else {
    var ratio = leaderScore / leaderMax;
    version = ratio < 0.33 ? 'low' : (ratio < 0.67 ? 'medium' : 'high');
  }

  return { leader: leader, version: version, scores: scores };
}

// Главна функция: финален нормализиран скор + combo логика
// Връща: { scores, pcts, noCerebro, winner, runnerUp, isCombo, sorted }
function computeFinalScores(answers, focusArea) {
  var allScoringIds = QUESTIONS
    .filter(function(q) { return q.type === 'radio' && q.id !== 'oncology'; })
    .map(function(q) { return q.id; });

  var scores = computePartialScores(answers, allScoringIds);

  // Проверка за онкология → Cerebro се изключва
  var noCerebro = false;
  for (var i = 0; i < QUESTIONS.length; i++) {
    if (QUESTIONS[i].id === 'oncology') {
      var oIdx = answers['oncology'];
      if (oIdx !== undefined && oIdx !== null) {
        var oAns = QUESTIONS[i].answers[oIdx];
        if (oAns && oAns.noCerebro) noCerebro = true;
      }
      break;
    }
  }
  if (noCerebro) scores.cerebro = 0;

  // Нормализация → реален %
  var keys  = ['extra', 'emotion', 'move', 'cerebro'];
  var total = keys.reduce(function(sum, k) { return sum + scores[k]; }, 0);
  var pcts  = {};

  if (total === 0) {
    keys.forEach(function(k) { pcts[k] = 25; });
  } else {
    keys.forEach(function(k) { pcts[k] = Math.round(scores[k] / total * 100); });
    // Коригира закръгляния (сумата трябва да е 100)
    var sum = keys.reduce(function(s, k) { return s + pcts[k]; }, 0);
    if (sum !== 100) {
      var maxKey = keys.reduce(function(a, b) { return pcts[b] > pcts[a] ? b : a; });
      pcts[maxKey] += (100 - sum);
    }
  }

  // Сортира по % (Cerebro изключен при онкология)
  var eligibleKeys = noCerebro
    ? keys.filter(function(k) { return k !== 'cerebro'; })
    : keys;
  var sorted = eligibleKeys.slice().sort(function(a, b) { return pcts[b] - pcts[a]; });

  var winner   = sorted[0];
  var runnerUp = sorted.length > 1 ? sorted[1] : null;

  // Combo: разлика ≤ 10% → показваме и двата продукта
  var isCombo = runnerUp !== null && (pcts[winner] - pcts[runnerUp]) <= 10;

  // Ако combo и focusArea е вторият → той получава приоритет (тайбрейкър)
  if (isCombo && focusArea && focusArea === runnerUp) {
    var tmp  = winner;
    winner   = runnerUp;
    runnerUp = tmp;
  }

  return {
    scores:   scores,
    pcts:     pcts,
    noCerebro: noCerebro,
    winner:   winner,
    runnerUp: isCombo ? runnerUp : null,
    isCombo:  isCombo,
    sorted:   sorted
  };
}

// Връща правилния TR блок за даден преход
// trNum:   1, 2 или 3
// answers: текущите отговори на потребителя
// focusArea: продуктът от Welcome страницата (fallback при 0 скор)
function getTRContent(trNum, answers, focusArea) {
  var trKey = 'tr' + trNum;

  // Кумулативни въпроси до тоя TR
  var cumulativeIds;
  if (trNum === 1) {
    cumulativeIds = QUESTION_GROUPS.G1.slice();
  } else if (trNum === 2) {
    cumulativeIds = QUESTION_GROUPS.G1.concat(QUESTION_GROUPS.G2);
  } else {
    cumulativeIds = QUESTION_GROUPS.G1.concat(QUESTION_GROUPS.G2).concat(QUESTION_GROUPS.G3);
  }

  var trInfo  = getTRInfo(answers, cumulativeIds);
  var product = trInfo.leader;
  var version = trInfo.version;

  // Fallback: ако всички скорове са 0 → ползваме focusArea
  var allZero = (trInfo.scores.extra + trInfo.scores.emotion +
                 trInfo.scores.move  + trInfo.scores.cerebro) === 0;
  if (allZero && focusArea) product = focusArea;

  return TR_CONTENT[trKey][product][version];
}

